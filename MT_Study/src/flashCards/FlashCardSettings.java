package flashCards;

import com.example.mt_study.R;

import android.app.Activity;
import android.os.Bundle;
import android.widget.ListView;

public class FlashCardSettings extends Activity {
	
	private ListView flashCardSettingsList;
	
	@Override
	public void onCreate(Bundle SavedInstanceState) {
		super.onCreate(SavedInstanceState);
		setContentView(R.layout.flash_card_settings);
		initControls();
	}
	
	
	public void initControls() {
		flashCardSettingsList = (ListView)findViewById(R.id.flash_card_settings_listView);
		flashCardSettingsList.setBackgroundColor(getResources().getColor(R.color.Black));
		
	}
	
	
	
}
